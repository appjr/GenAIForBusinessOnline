def main():
    script_dir = Path(__file__).parent

    # Load all batch files
    batch_files = [
        ("Text Generation Fundamentals — How LLMs Generate Text",
         "week07-slides-batch1.md"),
        ("OpenAI & Anthropic APIs, Prompt Engineering & Multi-Turn Chatbots",
         "week07-slides-batch2.md"),
        ("RAG, Guardrails & Deploying with Gradio",
         "week07-slides-batch3.md"),
        ("Business Use Cases, Platforms, Cost Analysis & Evaluation",
         "week07-slides-batch4.md"),
        ("Ethics, Case Studies, Lab Exercises & Key Takeaways",
         "week07-slides-batch5.md"),
    ]

    images_dir = script_dir / "images"
    images_present = images_dir.is_dir() and any(images_dir.glob("slide_*_final.png"))

    # Shared counter across all batches so slide_NNN numbers stay sequential
    slide_counter = {'n': 0}

    batch_contents = []
    for title, filename in batch_files:
        filepath = script_dir / filename
        if filepath.exists():
            with open(filepath, 'r', encoding='utf-8') as f:
                raw_content = f.read()
            html_content = markdown_to_html(
                raw_content,
                images_dir=images_dir if images_present else None,
                slide_counter=slide_counter if images_present else None,
            )
            batch_contents.append((title, html_content))
            print(f"✓ Processed {filename}")
        else:
            print(f"✗ Missing: {filename}")

    if not batch_contents:
        print("No batch files found!")
        return

    if images_present:
        n_imgs = len(list(images_dir.glob("slide_*.png")))
        print(f"✓ Found {n_imgs} slide image(s) in images/ — injecting into HTML")
    else:
        print("ℹ No slide images found — run generate_slide_images.py to add them")

    # Generate HTML
    html = create_html_document(batch_contents, images_present=images_present)

    output_path = script_dir / "Week07_Text_Generation_Chatbots_Complete.html"
    with open(output_path, 'w', encoding='utf-8') as f:
        f.write(html)

    size_kb = output_path.stat().st_size / 1024
    print(f"\n✅ HTML created: {output_path.name}")
    print(f"   Size: {size_kb:.1f} KB")
    print(f"   Batches: {len(batch_contents)}")


if __name__ == "__main__":
    main()
