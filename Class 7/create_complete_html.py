def markdown_to_html(text: str, images_dir: Path | None = None,
                     slide_counter: dict | None = None) -> str:
    """Convert markdown to HTML (simple implementation).

    If images_dir is provided, injects slide images after each ## Slide heading.
    slide_counter is a dict with key 'n' that persists across batch calls so the
    sequence matches slide_NNN_final.png filenames from generate_slide_images.py.
    """
    lines = text.split('\n')
    html_lines = []
    in_code_block = False
    code_lang = ''
    in_table = False
    in_list = False

    i = 0
    while i < len(lines):
        line = lines[i]

        # Code blocks
        if line.startswith('```'):
            if not in_code_block:
                in_code_block = True
                code_lang = line[3:].strip() or 'text'
                html_lines.append(f'<pre><code class="language-{code_lang}">')
            else:
                in_code_block = False
                html_lines.append('</code></pre>')
            i += 1
            continue

        if in_code_block:
            # Escape HTML entities in code
            escaped = line.replace('&', '&amp;').replace('<', '&lt;').replace('>', '&gt;')
            html_lines.append(escaped)
            i += 1
            continue

        # Tables
        if '|' in line and line.strip().startswith('|'):
            if not in_table:
                in_table = True
                html_lines.append('<table>')
                # Header row
                cells = [c.strip() for c in line.strip().strip('|').split('|')]
                html_lines.append('<thead><tr>')
                for cell in cells:
                    html_lines.append(f'<th>{cell}</th>')
                html_lines.append('</tr></thead><tbody>')
                i += 1
                # Skip separator row
                if i < len(lines) and re.match(r'[\|\s\-:]+', lines[i]):
                    i += 1
            else:
                cells = [c.strip() for c in line.strip().strip('|').split('|')]
                html_lines.append('<tr>')
                for cell in cells:
                    # Process inline formatting in cells
                    cell = re.sub(r'\*\*(.+?)\*\*', r'<strong>\1</strong>', cell)
                    cell = re.sub(r'\*(.+?)\*', r'<em>\1</em>', cell)
                    cell = re.sub(r'`(.+?)`', r'<code>\1</code>', cell)
                    html_lines.append(f'<td>{cell}</td>')
                html_lines.append('</tr>')
                i += 1
            continue
        elif in_table:
            in_table = False
            html_lines.append('</tbody></table>')

        # Headings
        if line.startswith('#### '):
            html_lines.append(f'<h4>{line[5:]}</h4>')
        elif line.startswith('### '):
            html_lines.append(f'<h3>{line[4:]}</h3>')
        elif line.startswith('## '):
            slide_text = line[3:]
            # Create anchor from slide text
            anchor = re.sub(r'[^a-z0-9]+', '-', slide_text.lower()).strip('-')
            html_lines.append(f'<h2 id="{anchor}" class="slide-title">{slide_text}</h2>')
            # Inject slide image using sequential counter (matches slide_NNN_final.png)
            if images_dir is not None and slide_text.lower().startswith('slide ') \
                    and slide_counter is not None:
                slide_counter['n'] += 1
                img_file = f"slide_{slide_counter['n']:03d}_final.png"
                img_path = images_dir / img_file
                if img_path.exists():
                    b64 = base64.b64encode(img_path.read_bytes()).decode("utf-8")
                    html_lines.append(
                        f'<div class="slide-image">'
                        f'<img src="data:image/png;base64,{b64}" alt="{slide_text}" '
                        f'loading="lazy">'
                        f'</div>'
                    )
        elif line.startswith('# '):
            html_lines.append(f'<h1>{line[2:]}</h1>')
        # Horizontal rule
        elif line.strip() == '---':
            html_lines.append('<hr class="slide-divider">')
        # Unordered lists
        elif re.match(r'^[\-\*] ', line):
            if not in_list:
                in_list = True
                html_lines.append('<ul>')
            item = line[2:]
            # Inline formatting
            item = re.sub(r'\*\*(.+?)\*\*', r'<strong>\1</strong>', item)
            item = re.sub(r'\*(.+?)\*', r'<em>\1</em>', item)
            item = re.sub(r'`(.+?)`', r'<code>\1</code>', item)
            item = re.sub(r'\[(.+?)\]\((.+?)\)', r'<a href="\2">\1</a>', item)
            html_lines.append(f'<li>{item}</li>')
        # Numbered lists
        elif re.match(r'^\d+\. ', line):
            if not in_list:
                in_list = True
                html_lines.append('<ol>')
            item = re.sub(r'^\d+\. ', '', line)
            item = re.sub(r'\*\*(.+?)\*\*', r'<strong>\1</strong>', item)
            item = re.sub(r'\*(.+?)\*', r'<em>\1</em>', item)
            item = re.sub(r'`(.+?)`', r'<code>\1</code>', item)
            html_lines.append(f'<li>{item}</li>')
        else:
            # Close lists
            if in_list:
                if html_lines and '<ul>' in '\n'.join(html_lines[-10:]):
                    # Find last opened ul/ol
                    for j in range(len(html_lines) - 1, -1, -1):
                        if html_lines[j] == '<ul>':
                            html_lines.append('</ul>')
                            break
                        elif html_lines[j] == '<ol>':
                            html_lines.append('</ol>')
                            break
                in_list = False

            # Blockquotes
            if line.startswith('> '):
                content = line[2:]
                content = re.sub(r'\*\*(.+?)\*\*', r'<strong>\1</strong>', content)
                html_lines.append(f'<blockquote>{content}</blockquote>')
            # Empty line = paragraph break
            elif line.strip() == '':
                html_lines.append('<br>')
            # Regular paragraph
            else:
                # Inline formatting
                line = re.sub(r'\*\*(.+?)\*\*', r'<strong>\1</strong>', line)
                line = re.sub(r'\*(.+?)\*', r'<em>\1</em>', line)
                line = re.sub(r'`(.+?)`', r'<code>\1</code>', line)
                line = re.sub(r'\[(.+?)\]\((.+?)\)', r'<a href="\2">\1</a>', line)
                line = re.sub(r'✅', '<span class="check">✅</span>', line)
                line = re.sub(r'❌', '<span class="cross">❌</span>', line)
                html_lines.append(f'<p>{line}</p>')

        i += 1

    # Close any open lists or tables
    if in_list:
        html_lines.append('</ul>')
    if in_table:
        html_lines.append('</tbody></table>')

    return '\n'.join(html_lines)


